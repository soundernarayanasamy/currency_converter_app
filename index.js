let convertcurrency = () => {
  const amount = document.querySelector(".currencyip").value;
  const fromcurrency = fromdropdown.value;
  const tocurrency = todropdown.value;

  if (amount.length != 0) {
    fetch(api)
      .then((resp) => resp.json())
      .then((data) => {
        let fromex = data.conversion_rates[fromcurrency];
        let toex = data.conversion_rates[tocurrency];
        const converted = (amount / fromex) * toex;
        document.querySelector(
          "#result .result"
        ).innerHTML = `${amount} ${fromcurrency} = ${converted.toFixed(
          2
        )} ${tocurrency}`;
      });
  }
};

document.querySelector("#btn").addEventListener("click", convertcurrency);
window.addEventListener("load", convertcurrency);
